#include <stdlib.h>
#include <iostream>
using namespace std;

int main() {
 srand48(time(NULL));
 int i, j, m = 1;
 double m1 = 0, m2 = 0, n = 1e7, s;
 for(i = 0; i < (int) n; ++i) {
  s = 0;
  for(j = 0; j < m; ++j) {
   s += drand48();
  }
  if(s > 1) {
   m1 += 1. / n;
   m2 += (s - 1) / n;
  }
 }
 cout.precision(40);
 cout.setf(ios::fixed,ios::floatfield);
 cout << "m1 = " << m1 << ", m2 = " << m2 << endl;
 return 0;
}

